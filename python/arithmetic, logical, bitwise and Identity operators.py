a, b = 2, 2
print("Arithmetic (addition): ", a + b)
print("Arithmetic (multiplication): ", a * b)
print("Logical operator (greater than): ", a > b)
print("Logical operator (less than): ", a < b)
print("Bitwise Operator (&): ", a & b)
